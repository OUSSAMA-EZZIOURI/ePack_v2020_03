    private void chooserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooserActionPerformed

        
        if (chooser.getSelectedFile() != null) {
            try {
                FileWriter fw = new FileWriter(chooser.getSelectedFile()+".csv");
                try (CSVPrinter printer = new CSVPrinter(fw, CSVFormat.DEFAULT
                        .withHeader("harness_part", "internal_part", "planned_qty"))) {
                    
                printer.printRecord("A","B","C");
                }
                fw.close();
                UILog.info("Fichier enregistr√© dans "+chooser.getSelectedFile());
                this.dispose();
            } catch (FileNotFoundException localFileNotFoundException) {
                localFileNotFoundException.printStackTrace();
                return;
            } catch (IOException localIOException) {
                localIOException.printStackTrace();
                return;
            }
        } else {
            this.dispose();
            //System.exit(0);
        }
    }//GEN-LAST:event_chooserActionPerformed

